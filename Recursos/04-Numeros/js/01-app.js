// Creación de números
const numero1 = 30; // Número entero positivo
const numero2 = 20; // Número entero positivo
const numero3 = 20.20; // Número decimal positivo
const numero4 = .102030; // Número decimal positivo con cero antes del punto decimal omitido
const numero5 = -20; // Número entero negativo

// Imprimir los números en la consola
console.log(numero1); // Imprime: 30
console.log(numero2); // Imprime: 20
console.log(numero3); // Imprime: 20.20
console.log(numero4); // Imprime: 0.102030
console.log(numero5); // Imprime: -20